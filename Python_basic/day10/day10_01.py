# 리스트

l1 = [ ] 
print(l1, type(l1))     # 빈 리스트

l2 = [1, ]
print(l2, type(l2))     # 1이란 요소값 하나만 갖는 리스트(이걸로 표현 x)

l3 = [1, '2', 3.14]     # 3개 요소값인 1개 리스트
print(l3, type(l3))

l4 = 1, '2', 3.14       # 대괄호 생략 불가능(튜플로 인식)
print(l4, type(l4))

l5 = ['a', 'b', [1.2, (3,4,5)], 'c']    #1차 리스트 안에 2차 리스트 안에
print(l5, type(l5))     # l5 : 요소값4개인 1개 리스트

ls = list("abcd")
lt = list((1,2,3, ('a', 'b')))
print(ls, type(ls))
print(lt, type(lt))