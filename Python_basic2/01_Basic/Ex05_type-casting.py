'''
자료형 ( data type)
- 데이터를 보관하고 있는 공간의 형식을 말합니다

type ( ) 
- 변수에 저장되어 있는 데이터의 형태를 확인하는 함수입니다

'''


'''
bool
- True (참), False(거짓)의 값을 가집니다.
'''

ba = True 
print("ba : ", ba)
print("ba type :", type(ba))
print()


'''
int (정수)
- 양수, 음수의 숫자 값
'''

ia = 10
print("ia : ", ia)
print("ia type : ", type(ia))
print()


'''
float(실수)
- 소수점을 사용하는 숫자 값
'''

fa = 1.2
print("fa : ", fa)
print("fa type : ", type(fa))
print()

'''
str (문자열)
- 문자들의 집합
'''

sa = "python"
print("sa : ", sa)
print("sa type : " ,type(sa))
print()
