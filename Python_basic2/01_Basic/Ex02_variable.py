'''
변수
- 데이터를 저장하는 공간
- 변수명을 사용해서 데이터를 사용하기 때문에 재사용이 가능

변수 선언(생성)
- 변수명 = 데이터 
    > 대입연산자( = ) : 오른쪽에 있는 값을 왼쪽에 넘겨주는 연산자입니다.

'''

print(123)

n = 123
print("n : ", n)

name = "손흥민"
print("name : ", name)

name = "이강인"
print("name : ", name)

age = 20
print("이름 : ", name)
print("나이 : ", age, " 세")
print()

a, b, c = 7, 1, 10
print("a : ", a, ", b :", b, ", c : ", c)

